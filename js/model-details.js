function generateStrengths(m) {
    var s = [];
    if (m.benchmarks.speed >= 8) s.push('Very fast inference');
    if (m.benchmarks.quality >= 8) s.push('High output quality');
    if (m.benchmarks.coding >= 8) s.push('Excellent coding abilities');
    if (m.benchmarks.reasoning >= 8) s.push('Strong reasoning capabilities');
    if (m.min_ram <= 8) s.push('Low RAM requirement — runs on most hardware');
    if (m.tags.indexOf('vision') !== -1) s.push('Multimodal vision support');
    if (m.released >= '2025-06') s.push('Recently released — latest architecture');
    if (s.length === 0) s.push('Solid general-purpose model', 'Good community support');
    return s;
}

function generateWeaknesses(m) {
    var w = [];
    if (m.benchmarks.speed <= 3) w.push('Slow inference — requires powerful hardware');
    if (m.benchmarks.quality <= 4) w.push('Limited output quality');
    if (m.min_ram >= 48) w.push('Very high RAM requirement');
    if (m.benchmarks.coding <= 4) w.push('Not suited for coding tasks');
    if (w.length === 0) w.push('May be surpassed by newer models');
    return w;
}

function generateUseCases(m) {
    var u = [];
    if (m.tags.indexOf('chat') !== -1) u.push('General chat assistant');
    if (m.tags.indexOf('code') !== -1) u.push('Code generation and review');
    if (m.tags.indexOf('reasoning') !== -1) u.push('Complex reasoning and analysis');
    if (m.tags.indexOf('vision') !== -1) u.push('Image understanding and description');
    if (m.tags.indexOf('general') !== -1) u.push('Multi-purpose AI assistant');
    if (m.tags.indexOf('math') !== -1) u.push('Mathematical problem solving');
    if (u.length === 0) u.push('General AI tasks');
    return u;
}
